def get_ganzhi_time():
    """获取当前时辰干支"""
    now = datetime.now()
    year = now.year
    month = now.month
    day = now.day
    hour = now.hour
    
    # 简化的干支计算（实际需要更复杂的历法）
    gan_index = (year - 4) % 10
    dizhi_index = (year - 4) % 12
    
    # 时辰
    shichen_hour = (hour + 1) // 2 % 12
    
    # 农历月（简化）
    month_gan = (month * 2 - 1) % 10
    
    # 日（简化）
    day_gan = (day * 3) % 10
    
    return {
        "year_gan": TIANGAN[gan_index] + DIZHI[dizhi_index],
        "month_gan": TIANGAN[month_gan] + "月",
        "day_gan": TIANGAN[day_gan] + "日",
        "shichen": DIZHI[shichen_hour] + "时",
        "shichen_gua": SHICHEN_GUA.get(DIZHI[shichen_hour], "坎")
    }

